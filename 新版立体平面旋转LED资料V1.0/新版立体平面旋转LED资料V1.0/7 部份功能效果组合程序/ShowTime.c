void Show_plane_time(void)//立体显示时间
{
		j++; 				           
        if( j>768 )//显示完文字后 循环； j=16*需要显示的字数 768/16=48
        {
            j=0;
        }
       for(i=j;i<ucNumRows+j;i++)  //显示190列 数据
        {                               
			if(m<8)
			{
				Show_L(shuziP[TimeBuff[m]][k*2],shuziP[TimeBuff[m]][k*2+1],0xFF,0XFF);//平面送时间显示
			}
			else if((m>7)&&(m<16))
			{
				Show_L(tab1[TimeBuff[m]][k*2],tab1[TimeBuff[m]][k*2+1],0xFF,0XFF);//平面送月日显示		
			}
			else
			{
				Show_L(0XFF,0XFF,0xFF,0XFF);
			}
			Show_R(Lzimo[i*2],Lzimo[i*2+1]);//侧面送16*16文字显示	
			Delay30us();//延时一段时间
		
						
           if(BIT_timeout==1)//这里如果没有显示完一圈的数据 回到起点了 
		                  //直接跳出 就不会出现头吃尾的现象 而且盲点很小
		   {
		   		return;
		   }
		    k++;
			 if(k>7)  //一个数字8列
			 {
			 	k=0;
				if((m<8)&&(Time==0))
				{
					m++;
				}
				if((m>7)&&(Time==0))  //间隔显示 时间 和日期 
				{
					m=20;
					if(l>0)
					l--;
					if(l==0)
					{
					  m=7;
					  Time=1;	
					}
					
				}																										   
				if((m<16)&&(Time==1))
				{
					m++;	
				}
			    if(m==16)
				m=20;
			    
		 }
		 Close();
		} 
		Time=0;
		l=6;
		k=0;
		m=0;
		Close(); 
	
}
