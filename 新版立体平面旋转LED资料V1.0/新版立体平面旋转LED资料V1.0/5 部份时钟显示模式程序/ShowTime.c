/*********************每列显示的时间************************************/
/*****n（us）延时子程序*****/
void Delay10us()		//@18.432MHz
{

    unsigned char a,b;
    for(b=153;b>0;b--)
        for(a=3;a>0;a--);
}
/****************************************/
void Delay30us()		//@18.432MHz
{

    unsigned char a,b;
    for(b=170;b>0;b--)
        for(a=3;a>0;a--);
}
